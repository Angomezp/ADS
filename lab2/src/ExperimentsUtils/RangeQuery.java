package lab2.src.ExperimentsUtils;

public class RangeQuery {

    private final int a;
    private final int b;

    public RangeQuery(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return this.a;
    }

    public int getB() {
        return this.b;
    }

}
